// Helper function to get unique states from schools data
export function getUniqueStates() {
    const statesSet = new Set(schoolsData.map(school => school.state));
    return Array.from(statesSet).sort();
}

// Helper function to filter schools by state
export function filterSchoolsByState(stateCode) {
    if (!stateCode) return schoolsData;
    return schoolsData.filter(school => school.state === stateCode);
}

// Helper function to search schools
export function searchSchools(query) {
    const lowerQuery = query.toLowerCase();
    return schoolsData.filter(school => 
        school.name.toLowerCase().includes(lowerQuery) ||
        school.abbr.toLowerCase().includes(lowerQuery) ||
        school.location.toLowerCase().includes(lowerQuery)
    );
}

// Helper function to get school by id
export function getSchoolById(id) {
    return schoolsData.find(school => school.id === id);
}

// Default export for convenience
export default schoolsData;